#include "3-calc.h"
/**
 * op_add - add tow number
 * @a:integer number 1
 * @b:integer number2
 * Return: always 0
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtruct tow number
 * @a:integer number 1
 * @b:integer number2
 * Return: always a - b
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiply tow number
 * @a:integer number 1
 * @b:integer number2
 * Return: always a * b
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divided tow number
 * @a:integer number 1
 * @b:integer number2
 * Return: always a / b
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulo tow number
 * @a:integer number 1
 * @b:integer number2
 * Return: always a % b
*/
int op_mod(int a, int b)
{
return (a % b);
}
