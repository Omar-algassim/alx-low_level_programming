#include "dog.h"
#include <stddef.h>

/**
 * *nwe_dog - creat new dog
 * @name: the name of new dog
 * @age:the age of new dog
 * @owner: the name of dog owner
 * Return: always 0
 */
dog_t *new_dog(char *name, float age, char *owner)
{
struct new_dog {
	name;
	age;
	owner;
};

typedef struct new_dog p;

if (p == NULL)
	return (NULL);
p->name;
p->age;
p->owner;

}


